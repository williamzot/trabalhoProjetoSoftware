    private void populateTable() {
        this.tablemodel.setNumRows(0);
        try {
            ResultSet rs = crud.getAvaliacoes();
            
            while (rs.next()) {
                this.tablemodel.addRow(new String[]{
                    rs.getString("SUBMISSAO"),
                    rs.getString("AVALIADOR"),
                    rs.getString("DATA_DO_PARECER"),
                    rs.getString("ARQUIVO"),
                    rs.getString("STATUS")
                });
                list.add(new AvaliacoesBanco(Integer.parseInt(rs.getString("ID")),
                rs.getString("SUBMISSAO"),
                rs.getString("AVALIADOR"),
                rs.getString("DATA_DO_PARECER"),
                rs.getString("ARQUIVO"),
                rs.getString("STATUS")));
            }
            this.index = list.getLast() .getId();
                       
        } catch (SQLException ex) {
            Logger.getLogger(Financas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

