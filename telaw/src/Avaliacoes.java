    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        try {
            int row = jTableAvaliacoes.getSelectedRow();
            
            UpdateAvaliacoes up = new UpdateAvaliacoes(this, true, list.get(row));
            up.setVisible(true);
            
            if (up.up == true) {
                list.remove(row);
                list.add(row, up.a);
                
                String[] new_row = new String[]{String.valueOf(up.a.getSubmissao()),
                    String.valueOf(up.a.getAvaliador()),
                    String.valueOf(up.a.getDataParecer()),
                    String.valueOf(up.a.getArquivoProjeto()),
                    String.valueOf(up.a.getStatus())};
                
                for (int i = 0; i < new_row.length; i++) {
                    tablemodel.setValueAt(new_row[i], row, i);
                    System.out.println(new_row[i]);
                }
            } 
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Selecione a linha da avaliação que deseja Editar!");
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int index = list.get(jTableAvaliacoes.getSelectedRow()).getId();
        System.out.println(index);
        if(crud.deleteAvaliacoes(index) == true){
            list.remove(jTableAvaliacoes.getSelectedRow());
            tablemodel.removeRow(jTableAvaliacoes.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(null, "Não foi possivel deletar esta linha.");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

